void main() {
  // ! Simple DataTypes

  // int
  // double
  // String
  // bool

  int a = 69;
  double b = 51.51;
  String c = 'Hello';
  bool d = true;
  print(a);
  print(b);
  print(c);
  print(d);

  // ! Complex DataTypes Collection's
  // List
  // set
  // Map

  List<dynamic> e = [1, 2, 3, 4, 5, 6];
  Set<dynamic> f = {1, 2, 3, 4, 55, 6}; // No repetation is allowed....
  Map<int, String> g = {1: 'a', 2: 'b', 3: 'd'};
  print(e);
  print(f);
  print(g);
}
